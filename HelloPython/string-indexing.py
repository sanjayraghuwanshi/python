course = 'Python for beginners'
print(course[0]) #print first character
print(course[0:3])  #print 0-3 characters
print(course[0:])   # print 0 to all
print(course[:5])   # print 0 to 5 characters
print(course[:])    # print all the characters
print(course[-0])   # Reverse index, prints first from last
print(course[1:-1]) #from 1 to -1, misses on first and last character.
print(course[::-1]) # reverse the whole string.